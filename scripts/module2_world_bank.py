COLUMNS = [
    "country_code", "country_name", "year",
    "gdp_usd_bn", "gdp_per_capita", "gdp_growth_rate",
    "internet_penetration", "unemployment_rate", "population_mn",
    "rd_expenditure_pct_gdp", "ease_of_doing_business_rank"
]


def load_world_bank_data() -> pd.DataFrame:
    df = pd.DataFrame(WORLD_BANK_DATA, columns=COLUMNS)
    df["pandemic_period"] = df["year"].apply(
        lambda y: "pre" if y < 2020 else ("during" if y <= 2021 else "post")
    )
    return df


def save_to_db(df: pd.DataFrame, conn: sqlite3.Connection):
    df.to_sql("world_bank_raw", conn, if_exists="replace", index=False)
    logger.info(f"Saved {len(df)} rows to world_bank_raw")


def run():
    logger.info("=== Module 2 Step 1: World Bank Data ===")
    df = load_world_bank_data()
    logger.info(f"Loaded {len(df)} records for {df['country_code'].nunique()} countries")

    # Save CSV
    save_path = "data/raw/world_bank_data.csv"
    from src.utils import get_project_root
    full_path = get_project_root() / save_path
    full_path.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(full_path, index=False)
    logger.info(f"Saved CSV → {save_path}")

    # Save to DB
    conn = get_db_connection()
    save_to_db(df, conn)
    conn.close()

    return df


if __name__ == "__main__":
    df = run()
    print(f"\n✓ World Bank data loaded: {df.shape}")
    print(df.groupby("pandemic_period")[["gdp_growth_rate", "internet_penetration"]].mean().round(2))
